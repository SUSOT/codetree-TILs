#include <iostream>
using namespace std;

int main() {
    int a,b;
    cin >> a;
    if(a % 2 == 0)
    {
        cout << "even";
    }
    else
    {
        cout << "odd";
    }
    cin >> b;
    if(b % 2 == 0)
    {
        cout << "even";
    }
    else
    {
        cout << "odd";
    }
    return 0;
}